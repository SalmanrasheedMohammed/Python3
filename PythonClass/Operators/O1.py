# ARITHMETIC OPERATORS
a = 10
b = 5

# Addition
print(a + b)  # Output: 15

# Subtraction
print(a - b)  # Output: 5

# Multiplication
print(a * b)  # Output: 50

# Division
print(a / b)  # Output: 2.0

# Floor Division
print(a // b) # Output: 2

# Modulus
print(a % b)  # Output: 0

# Exponentiation
print(a ** b) # Output: 100000

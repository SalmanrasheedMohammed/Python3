# BITWISE OPERATORS
a = 60  # 60 in binary is 0011 1100
b = 13  # 13 in binary is 0000 1101

# Bitwise AND
print(a & b)  # Output: 12 (0000 1100)

# Bitwise OR
print(a | b)  # Output: 61 (0011 1101)

# Bitwise XOR
print(a ^ b)  # Output: 49 (0011 0001)

# Bitwise NOT
print(~a)     # Output: -61

# Bitwise Left Shift
print(a << 2) # Output: 240 (1111 0000)

# Bitwise Right Shift
print(a >> 2) # Output: 15 (0000 1111)

# ASSINGNMENT OPERATORS
a = 10

# Add AND
a += 5  # Equivalent to a = a + 5

# Subtract AND
a -= 5  # Equivalent to a = a - 5

# Multiply AND
a *= 2  # Equivalent to a = a * 2

# Divide AND
a /= 2  # Equivalent to a = a / 2

# Modulus AND
a %= 3  # Equivalent to a = a % 3

# Exponent AND
a **= 2 # Equivalent to a = a ** 2

# Floor Division AND
a //= 3 # Equivalent to a = a // 3

# DATATYPES (INT,FLOAT,STRING,BOOLEAN,DICT,SET)

#INT

a = 10
b = 20

print (a, b) # 10 20
print (a+b) # 30
print (a-b) # -10
print (a*b) # 200
print (type(a)) # <class 'int'>


#FLOAT

ab = 100.00
bc = 200.00

print (ab, bc) # 100.0 200.0
print (type(ab)) # <class 'float'>

#STR 

s = "rasheed"
j = "jannu"

print (s, j) # rasheed jannu
print (type(s)) # <class 'str'>

#BOOL

ab = False
bc = True

print (ab) # False
print (bc) # True
print (type(ab)) # <class 'bool'>

#LIST

a = [ 0, 1, 2, 3, 4, 5]
b = [ 'a', 'b', 'c', 'd']

print (a) # [0, 1, 2, 3, 4, 5]
print (b) # ['a', 'b', 'c', 'd']
print (type(a)) # <class 'list'>

#DICT

person1 = {'name': 'rasheed','age': '23','gender': 'male','city': 'kakinada'}
person2 = {'name': 'jannu','age': '23','gender': 'male','city': 'pitapurama'}

print (person1) # {'name': 'rasheed', 'age': '23', 'gender': 'male', 'city': 'kakinada'}
print (person2) # {'name': 'jannu', 'age': '23', 'gender': 'male', 'city': 'pitapurama'}
print (type(person1)) # <class 'dict'>

#set

a = { 0, 1, 2, 3, 4, 5}

print (a) # {0, 1, 2, 3, 4, 5}
print (type(a)) # <class 'set'>
 
